const os = require('os');

// Platform
console.log(os.platform());

// Architecture
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// free Memory
console.log(os.freemem());

// total Memory
console.log(os.totalmem());

// Home directory
console.log(os.homedir());

// uptime
console.log(os.uptime());
